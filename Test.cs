		public Test( Mode modeParam, TextWriter errorOutParam, TextWriter infoOutParam, out bool error )
		{
			_mode = modeParam;
			_errorOut = errorOutParam;
			_infoOut = infoOutParam;
			
			// Validate parsing, this whole program expects little endian
			{
				uint val = 1234567891;
				if( Convert.ToUInt32( ToBinary( Convert.ToUInt32( ToBinary( val ), 2 ) ), 2 ) != val )
					ReportError( $"{nameof(ToBinary)} and back failed" );
			}
			
			for( int testIndex = 0; testIndex < TESTS.Length; testIndex++ )
			{
				for( int testResult = 0; testResult < TESTS[testIndex].results.Length; testResult++ )
				{
					var result = TESTS[ testIndex ].results[ testResult ];
					var asFloat = To<uint, float>( result.i );
					if( asFloat != result.f && ! NaNAndBitsMatch( asFloat, result.f ) )
						ReportError( $"FAILED PARSING {ToBinary(result.i)} to value {result.f:G9}, expected {ToBinary(result.f)}" );
				}
			}
			for( int testIndex = 0; testIndex < TESTS.Length; testIndex++ )
			{
				float value = To<uint, float>( TESTS[testIndex].initialValue );
				
				// Certain operations are funneling tests into specific ranges of values
				// so we aren't just using them as is, that is dVal's purpose in this code
				
				int checkIndex = 0;
				Validate( value, checkIndex++, testIndex, out _ );
				Validate( value * _mult, checkIndex++, testIndex, out _ );
				Validate( value + _add * value, checkIndex++, testIndex, out _ );
				Validate( value / _div, checkIndex++, testIndex, out _ );
				Validate( value - _sub * value, checkIndex++, testIndex, out _ );
				Validate( value % 7.0f, checkIndex++, testIndex, out _ ); 
				Validate( MathF.Abs( value ), checkIndex++, testIndex, out _ );
				Validate( MathF.Acos( value % 1f ), checkIndex++, testIndex, out _ );
				Validate( MathF.Acosh( 1f + MathF.Abs( value ) ), checkIndex++, testIndex, out _ );
				Validate( MathF.Asin( value % 1f ), checkIndex++, testIndex, out _ );
				Validate( MathF.Asinh( value ), checkIndex++, testIndex, out _ );
				Validate( MathF.Atan( value ), checkIndex++, testIndex, out _ );
				Validate( MathF.Atan2( value, 0.17f ), checkIndex++, testIndex, out _ );
				Validate( MathF.Atanh( value % 1f ), checkIndex++, testIndex, out _ );
				Validate( MathF.Cbrt( value ), checkIndex++, testIndex, out _ );
				Validate( MathF.Ceiling( value ), checkIndex++, testIndex, out _ );
				Validate( MathF.Cos( value ), checkIndex++, testIndex, out _ );
				Validate( MathF.Cosh( value % 2f ), checkIndex++, testIndex, out _ );
				Validate( MathF.Exp( 1f / value ), checkIndex++, testIndex, out _ );
				Validate( MathF.Floor( value ), checkIndex++, testIndex, out _ );
				Validate( MathF.FusedMultiplyAdd( value, 1.33f, -1.5f ), checkIndex++, testIndex, out _ );
				Validate( MathF.IEEERemainder( value, 25.78f ), checkIndex++, testIndex, out _ );
				Validate( MathF.Log( MathF.Abs( value ) ), checkIndex++, testIndex, out _ );
				Validate( MathF.Log( MathF.Abs( value ), 4f ), checkIndex++, testIndex, out _ );
				Validate( MathF.Log2( MathF.Abs( value ) ), checkIndex++, testIndex, out _ );
				Validate( MathF.Log10( MathF.Abs( value ) ), checkIndex++, testIndex, out _ );
				Validate( MathF.Pow( MathF.Abs( value ) % 1E+23f, 1.7f ), checkIndex++, testIndex, out _ );
				Validate( MathF.ScaleB( value % 1E+23f, 2 ), checkIndex++, testIndex, out _ );
				Validate( MathF.Sin( value ), checkIndex++, testIndex, out _ );
				Validate( MathF.Sinh( value % 2f ), checkIndex++, testIndex, out _ );
				Validate( MathF.Sqrt( MathF.Abs( value ) ), checkIndex++, testIndex, out _ );
				Validate( MathF.Tan( value ), checkIndex++, testIndex, out _ );
				Validate( MathF.Tanh( value ), checkIndex++, testIndex, out _ );
				Validate( MathF.Max( value, _mult ), checkIndex++, testIndex, out _ );
				Validate( MathF.MaxMagnitude( value, _add ), checkIndex++, testIndex, out _  );
				Validate( MathF.Min( value, _div ), checkIndex++, testIndex, out _  );
				Validate( MathF.MinMagnitude( value, _sub ), checkIndex++, testIndex, out _  );
				Validate( MathF.Round( value ), checkIndex++, testIndex, out _  );
				Validate( MathF.Sign( value ), checkIndex++, testIndex, out _  );
				Validate( MathF.Truncate( value ), checkIndex++, testIndex, out _  );
			}

			error = _containsError;
		}
		
		void ReportError<T>( T data )
		{
			_containsError = true;
			_errorOut?.WriteLine( data ? .ToString() );
		}
		
		void Validate( float result, int checkIndex, int currentTest, out float expectedVal )
		{
			if( _mode == Mode.Validate )
			{
				uint resultI = To<float, uint>( result );
				var expected = TESTS[ currentTest ].results[ checkIndex ];
				if( expected.i != resultI || expected.f != result && ! NaNAndBitsMatch( expected.f, result ) )
				{
					string resultAsBin = FloatToSpecializedFormatting( To<uint, float>( resultI ) );
					string expectedAsBin = FloatToSpecializedFormatting( To<uint, float>( expected.i ) );
					string xorAsBin = FloatToSpecializedFormatting( To<uint, float>( resultI ^ expected.i ) );
					ReportError( $"FAILED {CheckNames[checkIndex]} on test {currentTest}\n\t{expectedAsBin}({expected.f:G9}) expected\n\t{resultAsBin}({result:G9}) got\n\t{xorAsBin} XOR" );
				}
				expectedVal = expected.f;
			}
			else
			{
				string resultAsBin = FloatToSpecializedFormatting( result );
				if(checkIndex == 0)
					_infoOut?.WriteLine( $"( {resultAsBin}, new (uint, float)[]\n{{" );
				string resultString;
				if( float.IsPositiveInfinity( result ) )
					resultString = "float.PositiveInfinity";
				else if( float.IsNegativeInfinity( result ) )
					resultString = "float.NegativeInfinity";
				else if( float.IsNaN( result ) )
					resultString = $"To<uint, float>({resultAsBin})";
				else
					resultString = $"{result:G9}f";
				
				_infoOut?.WriteLine( $"\t({FloatToSpecializedFormatting( result )}, {resultString})," );
				if(checkIndex == CheckNames.Length-1)
					_infoOut?.WriteLine( $"}} )," );
				expectedVal = result;
			}
		}
		
